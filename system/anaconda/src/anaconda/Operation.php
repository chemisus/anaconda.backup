    public function __construct($permissions=array()) {
        foreach ((array)$permissions as $key=>$value) {
            if (is_numeric($key)) {
                $this->permissions[$value] = $this;
            }
            else if (is_object($value)) {
                $this->permissions[$key] = array($value);
            }
            else {
                $this->permissions[$key] = (array)$value;
            }
        }
    }
